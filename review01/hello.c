/**
 * 变量申明及输入占位符!!
 */
#include <stdio.h>

int main(){
    int num = 10;
    printf("num变量的值%d\n",num);

    double a = 1.22;
    printf("a变量的值%f\n",a);

    float b = 3.2323f;
    printf("b变量的值%lf\n",b);

    char c = 'a'; //并且单引号修饰
    printf("c变量的值%c\n",c);




    return 0;
}
